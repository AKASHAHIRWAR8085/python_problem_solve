# type casting is the process of converting one data type to another data type.
# There are two types of type casting in python:
# 1. Implicit type casting: It is the process of converting one data type to another data type automatically by the python interpreter.
# 2. Explicit type casting: It is the process of converting one data type to another data type manually by the programmer.
# int
# a =30
# t= type(a) 
# print(t)

# float
# a =30.3
# t= type(a) 
# print(t)

# string
